<h5><p align="justify">Stunting adalah kondisi dimana balita mengalami gagal tumbuh dimasa pertumbuhan pada anak seusianya, terutama pada periode usia balita. Baik pada masalah gizi anak atau tinggi badan yang kurang dibandingkan dengan umur. Kondisi ini diukur dengan panjang atau tinggi badan yang lebih dari minus dua standar deviasi median standar pertumbuhan anak dari WHO.</p></h5>
<h5><p align="justify">Dampak yang ditimbulkan stunting dapat dibagi menjadi dampak jangka pendek dan jangka panjang. Dampak jangka pendek yaitu peningkatan kejadian kesakitan dan kematian, perkembangan kognitif, motorik, dan verbal pada anak tidak optimal, dan peningkatan biaya kesehatan. Sedangkan, dalam jangka panjang yaitu postur tubuh yang tidak optimal saat dewasa (lebih pendek dibandingkan pada umumnya), meningkatnya risiko obesitas dan penyakit lainnnya, menurunnya kesehatan reproduksi, kapasitas belajar dan performa yang kurang optimal saat masa sekolah, dan produktivitas dan kapasitas kerja yang tidak optimal.</p></h5>